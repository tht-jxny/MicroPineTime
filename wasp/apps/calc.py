"""Ultra-simple settings application.

Currently the settings application contains only one setting: brightness
"""

import wasp
import parser

class CalculatorApp():
    NAME = 'Calc'
    ICON = calc

    def __init__(self):
        self.output = ""
        self.fields = [["7","8","9","+","("],
                        ["4","5","6","-",")"],
                        ["1","2","3","*","^"],
                        ["C","0",".",":","="]]
        self.fields_to_button_path = {"0":calc_num_0,
                                      "1":calc_num_1,
                                      "2":calc_num_2,
                                      "3":calc_num_3,
                                      "4":calc_num_4,
                                      "5":calc_num_5,
                                      "6":calc_num_6,
                                      "7":calc_num_7,
                                      "8":calc_num_8,
                                      "9":calc_num_9,
                                      "+":calc_symbol_plus,
                                      "-":calc_symbol_minus,
                                      "*":calc_symbol_multi,
                                      ":":calc_symbol_divide,
                                      ")":calc_symbol_close,
                                      "(":calc_symbol_open,
                                      "^":calc_symbol_hat,
                                      ".":calc_symbol_point,
                                      "=":calc_symbol_equal,
                                      "C":calc_symbol_new}
    
    def foreground(self):
        self.drawButtons()
        wasp.system.request_event(wasp.EventMask.TOUCH)

    def touch(self, event):
        if (event[2] < 48):
            if (event[1] > 200): # undo button pressed
                if (self.output != ""):
                    self.output = self.output[:-1]
        else:
            indices = [(event[2]// 48)-1,event[1]//47]
            # Error handling for touching at the border
            if (indices[0]>3):
                indices[0] = 3
            if (indices[1]>4):
                indices[1] = 4
            button_pressed = self.fields[indices[0]][indices[1]]
            if (button_pressed == "C"):
                self.output = ""
            elif (button_pressed == "="):
                self.output = self.calculate(self.output)
            else:
                self.output +=  button_pressed
        self.displayOutput()

    def _draw(self):
        self.drawButtons()

    def drawButtons(self):
        wasp.watch.drawable.fill()
        for y in range(4):
            for x in range(5):
                icon = self.fields_to_button_path[self.fields[y][x]]
                if (x == 0):
                    wasp.watch.drawable._rle2bit(icon, x*47,y*47+46)
                else:
                    wasp.watch.drawable._rle2bit(icon, x*47-1,y*47+46)
        wasp.watch.drawable._rle2bit(calc_symbol_undo, 205,2)


    def displayOutput(self):
        wasp.watch.drawable.fill(x=2,y=2,w=195,h=40) 
        if (self.output != ""):
            if len(self.output) >= 11:
                wasp.watch.drawable.string(self.output[len(self.output)-11:], 5, 8, width=190)
            else:
                wasp.watch.drawable.string(self.output, 5, 8, width=190)

    def calculate(self,s):
        equation = s
        for i in range(len(s)):
            if (s[i] =="^"):
                equation = s[:i] + "**"+s[i+1:]
        
        try:
            result = eval(parser.expr(equation).compile())
        except:
            print("Error")
            result = ""
        
        return str(result)
